    def __init__(self, page=3):
        self.proxies = []
        self.verify_pro = []
        self.page = page
        agent = random.choice(Proxies.MY_USER_AGENT)
        self.headers = {
            'Accept': '*/*',
            'User-Agent': agent,
            'Accept-Encoding': 'gzip, deflate, sdch',
            'Accept-Language': 'zh-CN,zh;q=0.8'
        }
        self.get_proxies()
        self.get_proxies_nn()

    def set_proxies(self, usable_ip_list):
        for ip in usable_ip_list:
            self.proxies.append(ip)

    def get_proxies(self):
        # page = random.randint(1, 1)
        page = 1
        page_stop = page + self.page
        while page < page_stop:
            url = 'http://www.xicidaili.com/nt/%d' % page
            print(url)
            html = requests.get(url, headers=self.headers).content

            soup = BeautifulSoup(html, 'lxml')
            ip_list = soup.find(id='ip_list')

            for odd in ip_list.find_all(class_='odd'):
                protocol = odd.find_all('td')[5].get_text().lower() + '://'
                self.proxies.append(protocol + ':'.join([x.get_text() for x in odd.find_all('td')[1:3]]))
            page += 1

    def get_proxies_nn(self):
        page = 1
        page_stop = page + self.page
        while page < page_stop:
            url = 'http://www.xicidaili.com/nn/%d' % page
            html = requests.get(url, headers=self.headers).content
            soup = BeautifulSoup(html, 'lxml')
            ip_list = soup.find(id='ip_list')
            for odd in ip_list.find_all(class_='odd'):
                protocol = odd.find_all('td')[5].get_text().lower() + '://'
                self.proxies.append(protocol + ':'.join([x.get_text() for x in odd.find_all('td')[1:3]]))
            page += 1

    def verify_proxies(self):
        # 没验证的代理  
        old_queue = Queue()
        # 验证后的代理  
        new_queue = Queue()
        print('verify proxy........')
        works = []
        for _ in range(5):
            works.append(Process(target=self.verify_one_proxy, args=(old_queue, new_queue)))
        for work in works:
            work.start()
        for proxy_item in self.proxies:
            old_queue.put(proxy_item)
        for work in works:
            old_queue.put(0)
        for work in works:
            work.join()
        self.proxies = []
        while 1:
            try:
                self.proxies.append(new_queue.get(timeout=1))
            except Exception as e:
                print(e)
                break
        print('verify_proxies done!')

    def verify_one_proxy(self, old_queue, new_queue):
        while 1:
            v_proxy = old_queue.get()
            if v_proxy == 0:
                break
            protocol = 'https' if 'https' in v_proxy else 'http'
            proxies = {protocol: v_proxy}
            try:
                if requests.get('http://www.baidu.com', proxies=proxies, timeout=2).status_code == 200:
                    print('success %s' % v_proxy)
                    new_queue.put(v_proxy)
            except Exception as e:
                print('fail %s' % v_proxy)

    def main_start(self):
        self.verify_proxies()
        return self.proxies


